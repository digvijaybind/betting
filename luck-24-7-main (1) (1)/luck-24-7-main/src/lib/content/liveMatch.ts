export type ScoreType = {
  [key: string]: {
    batting: {
      score: {
        runs: number;
        balls: number;
        fours: number;
        sixes: number;
        dot_balls: number;
        strike_rate: number;
      };
      dismissal: {
        overs: number[];
        team_runs: number;
        wicket_number: number;
        ball_key: string;
      } | null;
    } | null;
    bowling: {
      score: {
        balls: number;
        runs: number;
        economy: number;
        extras: number;
        wickets: number;
        maiden_overs: number;
        overs: number[];
        balls_breakup: {
          dot_balls: number;
          wides: number;
          no_balls: number;
          fours: number;
          sixes: number;
        };
      };
    } | null;
    fielding: { catches: number; stumpings: number; runouts: number } | null;
  };
};

export type PlayerDetails = {
  player: PlayerBasicDetails;
  score: ScoreType;
};

